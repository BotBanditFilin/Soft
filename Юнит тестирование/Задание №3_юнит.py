import re

def is_correct_mobile_phone_number_ru(number):
    pattern = re.compile(r'^(8|\+7)[-\s]?\(?\d{3}\)?[-\s]?\d{3}[-\s]?\d{2}[-\s]?\d{2}$')
    return bool(pattern.match(number))

def test_is_correct_mobile_phone_number_ru():
    tests = [
        "+79991234567",             # Корректный номер без форматирования
        "8(999)1234567",            # Корректный номер с кодом оператора в скобках
        "+7 999 123 45 67",        # Корректный номер с пробелами
        "+7 999-123-45-67",        # Корректный номер с дефисами
        "+7(999) 1234567",         # Корректный номер с пробелами и кодом оператора в скобках
        "+7(999)123-45-67",        # Корректный номер с кодом оператора в скобках и дефисами
        "+7 9991234567",           # Корректный номер без пробелов
        "+7 (999) 123 45 67",      # Корректный номер с пробелами и кодом оператора в скобках
        "+7 (999) 123-45-67",      # Корректный номер с пробелами и кодом оператора в скобках и дефисами
        "+7(999)1234567",          # Корректный номер с кодом оператора в скобках без разделителей
        "+7999 123 45 67",         # Некорректный номер: пробелы без скобок для кода оператора
        "+7 9991234 567",          # Некорректный номер: пробелы без дефисов или скобок
        "+8(999)1234567",          # Некорректный номер: неверный код страны
        "+7(99)1234567",           # Некорректный номер: неверный формат кода оператора
        "79991234567",             # Некорректный номер: отсутствует код страны
        "+7(999)123456789"         # Некорректный номер: слишком много цифр после кода оператора
    ]

    for test in tests:
        result = is_correct_mobile_phone_number_ru(test)
        if not result:
            print("NO")
            return
    
    print("YES")
    
test_is_correct_mobile_phone_number_ru()
